def concatenate(str1, str2):
    """
    Concatène deux chaînes de caractères.
    Args:
        str1 : La première chaîne.
        str2 : La seconde chaîne.
    Returns:
        str: La concaténation des deux chaînes.
    """
    return str1 + str2

def to_uppercase(string):
    """
    Convertit une chaîne de caractères en majuscules.
    Args:
        string : La chaîne à convertir.
    Returns:
        str: La chaîne en majuscules.
    """
    return string.upper()

